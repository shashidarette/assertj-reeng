// Metric enumeration
public enum Metric {
	LOC,			// Lines of code
	WMC, 			// WeightedMethodCount
	CC,  			// Cyclomatic Complexity
	MethodCount, 	// Number of methods
	AttributeCount, // Number of attributes in a class
	FieldCount, 	// Number of fields
	DepthOfInheritance, // Depth of inheritance
}
